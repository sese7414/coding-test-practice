/*
문제: 경기도에 위치한 식품창고 목록 출력하기
분류: IS NULL
링크: https://school.programmers.co.kr/learn/courses/30/lessons/131114
*/

-- 방법1
SELECT  WAREHOUSE_ID,
        WAREHOUSE_NAME,
        ADDRESS,
        IF(FREEZER_YN IS NULL, 'N', FREEZER_YN) FREEZER_YN
FROM FOOD_WAREHOUSE
WHERE ADDRESS LIKE '경기도%'
ORDER BY WAREHOUSE_ID
  
-- 방법2
SELECT  WAREHOUSE_ID,
        WAREHOUSE_NAME,
        ADDRESS,
        ifnull(FREEZER_YN, 'N') FREEZER_YN
FROM FOOD_WAREHOUSE
WHERE ADDRESS LIKE '경기도%'
ORDER BY WAREHOUSE_ID

-- 방법3
SELECT  WAREHOUSE_ID,
        WAREHOUSE_NAME,
        ADDRESS,
        COALESCE(FREEZER_YN, 'N') FREEZER_YN
FROM FOOD_WAREHOUSE
WHERE ADDRESS LIKE '경기도%'
ORDER BY WAREHOUSE_ID
